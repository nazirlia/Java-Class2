public class Loop_Task1 {
    public static void main(String[] args) {

        whileLoop();
    }
    public static void forLoop(){
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
    public static void whileLoop(){
        int i = 1;
        while (i <= 10){
            System.out.println(i);
            i++;
        }
    }
}
